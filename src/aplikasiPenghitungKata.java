    private void hitungOtomatis() {
    String teks = txtInput.getText();
    String teksTrim = teks.trim();

    // Hitung jumlah kata
    String[] kata = teksTrim.split("\\s+");
    int jumlahKata = teksTrim.isEmpty() ? 0 : kata.length;

    // Hitung karakter tanpa spasi
    int jumlahKarakter = teks.replaceAll("\\s", "").length();

    // Hitung spasi
    int jumlahSpasi = 0;
    for (int i = 0; i < teks.length(); i++) {
        if (Character.isWhitespace(teks.charAt(i))) {
            jumlahSpasi++;
        }
    }

    // Hitung tanda baca
    int jumlahTandaBaca = 0;
    for (int i = 0; i < teks.length(); i++) {
        char c = teks.charAt(i);
        if (!Character.isLetterOrDigit(c) && !Character.isWhitespace(c)) {
            jumlahTandaBaca++;
        }
    }

    // Hitung kalimat
    String[] kalimat = teksTrim.split("[.!?]+");
    int jumlahKalimat = teksTrim.isEmpty() ? 0 : kalimat.length;

    // Hitung paragraf
    String[] paragraf = teksTrim.split("\\n+");
    int jumlahParagraf = teksTrim.isEmpty() ? 0 : paragraf.length;

    // Total keseluruhan karakter (semua termasuk spasi & tanda baca)
    int totalKeseluruhan = teks.length();

    // ðŸ”¹ Tampilkan hasil ke label
    lblJumlahKata.setText(String.valueOf(jumlahKata));
    lblJumlahKarakter.setText(String.valueOf(jumlahKarakter));
    lblJumlahKalimat.setText(String.valueOf(jumlahKalimat));
    lblJumlahParagraf.setText(String.valueOf(jumlahParagraf));
    lblJumlahSpasi.setText(String.valueOf(jumlahSpasi));
    lblJumlahTandaBaca.setText(String.valueOf(jumlahTandaBaca));
    lblTotalKeseluruhan.setText(String.valueOf(totalKeseluruhan));
}

