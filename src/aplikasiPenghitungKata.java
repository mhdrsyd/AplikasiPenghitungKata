    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
            JFileChooser chooser = new JFileChooser();
    chooser.setDialogTitle("Simpan hasil ke file teks (.txt)");
    int userSelection = chooser.showSaveDialog(this);

    if (userSelection == JFileChooser.APPROVE_OPTION) {
        File fileToSave = chooser.getSelectedFile();

        // Tambahkan ekstensi .txt jika belum ada
        if (!fileToSave.getAbsolutePath().toLowerCase().endsWith(".txt")) {
            fileToSave = new File(fileToSave.getAbsolutePath() + ".txt");
        }

        try (BufferedWriter writer = new BufferedWriter(new FileWriter(fileToSave))) {
            writer.write("=== APLIKASI PENGHITUNG KATA ===\n\n");
            writer.write("Teks Asli:\n" + txtInput.getText() + "\n\n");
            writer.write("=== HASIL PERHITUNGAN ===\n");
            writer.write("Jumlah Kata               : " + lblJumlahKata.getText() + "\n");
            writer.write("Jumlah Karakter (tanpa spasi): " + lblJumlahKarakter.getText() + "\n");
            writer.write("Jumlah Spasi              : " + lblJumlahSpasi.getText() + "\n");
            writer.write("Jumlah Tanda Baca         : " + lblJumlahTandaBaca.getText() + "\n");
            writer.write("Jumlah Kalimat            : " + lblJumlahKalimat.getText() + "\n");
            writer.write("Jumlah Paragraf           : " + lblJumlahParagraf.getText() + "\n");
            writer.write("Total Keseluruhan Karakter: " + lblTotalKeseluruhan.getText() + "\n");
            writer.write("\nFile disimpan di: " + fileToSave.getAbsolutePath());

            JOptionPane.showMessageDialog(this,
                    "Berhasil disimpan di:\n" + fileToSave.getAbsolutePath(),
                    "Sukses", JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this,
                    "Gagal menyimpan file: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

