    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
         String teks = txtInput.getText().toLowerCase();
    String cari = jTextField1.getText().toLowerCase();

    if (cari.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Masukkan kata yang ingin dicari!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    String[] kata = teks.split("\\s+");
    int jumlahDitemukan = 0;

    for (String k : kata) {
        if (k.equals(cari)) {
            jumlahDitemukan++;
        }
    }

    JOptionPane.showMessageDialog(this,
        "Kata \"" + cari + "\" ditemukan sebanyak " + jumlahDitemukan + " kali.",
        "Hasil Pencarian", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnCariActionPerformed

